namespace RSBot.Training.Bundle.Movement;

internal class MovementConfig
{
    /// <summary>
    ///     Gets or sets a value indicating whether [walk to center].
    /// </summary>
    /// <value>
    ///     <c>true</c> if [walk to center]; otherwise, <c>false</c>.
    /// </value>
    public bool WalkToCenter { get; set; }

    /// <summary>
    ///     Gets or sets a value indicating whether [walk around].
    /// </summary>
    /// <value>
    ///     <c>true</c> if [walk around]; otherwise, <c>false</c>.
    /// </value>
    public bool WalkAround { get; set; }
}
