/// <summary>
/// Defines the available button combinations for the message box
/// </summary>
public enum MessageBoxButtons
{
    /// <summary>
    /// Shows only an OK button
    /// </summary>
    Ok,
    OK,

    /// <summary>
    /// Shows Yes and No buttons
    /// </summary>
    YesNo,

    /// <summary>
    /// Shows Yes, No, and Cancel buttons
    /// </summary>
    YesNoCancel,

    /// <summary>
    /// Shows OK and Cancel buttons
    /// </summary>
    OkCancel
}
